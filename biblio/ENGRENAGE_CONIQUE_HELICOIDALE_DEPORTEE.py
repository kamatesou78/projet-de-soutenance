        try:

            M = float(self.lineEdit1.text())
            A_P = float(self.lineEdit2.text())
            A_H = float(self.lineEdit2_1.text())
            Σ = float(self.lineEdit3.text())
            b = float(self.lineEdit4.text())    
            Z1 = int(self.lineEdit5.text())
            Z2 = int(self.lineEdit6.text())
            x1 = float(self.lineEdit7.text())
            x2 = float(self.lineEdit8.text())             
            xs1 = float(self.lineEdit8_1.text())
            xs2 = float(self.lineEdit8_2.text()) 

            C = 0

            Jn = 0

            U = Z2/Z1

            i = Z1/Z2

            d1 = Z1*M 
            d2 = Z2*M   

            A_P_A = math.atan(math.tan(math.radians(A_P))/math.cos(math.radians(A_H)))
            αt = math.degrees(A_P_A)

            ha1 = float(1+x1)*M
            ha2 = float(1+x2)*M

            hf1 = float(1+C-x1)*M
            hf2 = float(1+C-x2)*M

            h = 2.25*M

            δ11 = math.atan(math.sin(math.radians(Σ)) / ((Z2/Z1)+(math.cos(math.radians(Σ)))))
            δ1 = math.degrees(δ11)

            δ22 = math.atan(math.sin(math.radians(Σ)) / ((Z1/Z2)+(math.cos(math.radians(Σ)))))
            δ2 = math.degrees(δ22)

            zr1 = Z1/math.cos(math.radians(δ1))
            zr2 = Z2/math.cos(math.radians(δ2))

            R = (0.5*d1) / math.sin(math.radians(δ1))

            θ11 = math.atan(ha1/R)
            θa1 = math.degrees(θ11)

            θ22 = math.atan(ha2/R)
            θa2 = math.degrees(θ22)

            θf11 = math.atan(hf1/R)
            θf1 = math.degrees(θf11)

            θf22 = math.atan(hf2/R)
            θf2 = math.degrees(θf22)

            δa1 = δ1 + θa1

            δa2 = δ2 + θa2

            δf1 = δ1 - θf1

            δf2 = δ2 - θf2

            Rm = R - b/2

            Ri = R - b

            mnt = (M*Rm)/R

            sin1 = math.asin(math.sin(math.radians(A_H))/( 1-( (0.5*b) /R) ) )
            βm = math.degrees(sin1)
            sin2 = math.asin(math.sin(math.radians(A_H))/( (1- (b/R) ) ) )
            βi = math.degrees(sin2)

            se1 = M*((math.pi/2)+ 2* (x1* math.tan(math.radians(A_P))+xs1)) - 0.5*Jn
            se2 = M*((math.pi/2)+ 2* (x2* math.tan(math.radians(A_P))+xs2)) - 0.5*Jn

            SCe1 = se1*(1-C*(se1/d1)**2)
            SCe2 = se2*(1-C*(se2/d2)**2)

            A= 0.5*(se1/R)
            B= 0.5*(se2/R)
            ψP1= math.degrees(A)
            ψP2= math.degrees(A)

            scn1 = se1*math.cos(math.radians(A_H+ψP1))
            scn2 = se2*math.cos(math.radians(A_H+ψP2))

            ham1 = ha1 + 0.25*((SCe1**2)/d1)*math.cos(math.radians(δ1))
            ham2 = ha2 + 0.25*((SCe2**2)/d2)*math.cos(math.radians(δ2))

            da1 =  d1 + 2* ha1 *math.cos(math.radians(δ1))
            da2 =  d2 + 2* ha2 *math.cos(math.radians(δ2))

            df1 = d1 -2* hf1* math.cos(math.radians(δ1))
            df2 = d2 -2* hf2* math.cos(math.radians(δ2))

            dai1 = da1* (Ri/R)
            dai2 = da2* (Ri/R)

            zv1 = Z1/(math.cos(math.radians(δ1))*(math.cos(math.radians(A_H)))**3)
            zv2 = Z2/(math.cos(math.radians(δ2))*(math.cos(math.radians(A_H)))**3)

            dv1 = zv1*M
            dv2 = zv2*M

            av = (dv1 + dv2)/ 2

            dfv1 = dv1-2*hf1
            dfv2 = dv2-2*hf2

            dav1 = dv1+2*ha1
            dav2 = dv2+2*ha2

            dbv1 = dv1*math.cos(math.radians(A_P))
            dbv2 = dv2*math.cos(math.radians(A_P))

            αav11 = float(math.acos((dbv1/dav1)))
            αav22 = float(math.acos((dbv2/dav2)))
            αav1 = math.degrees(αav11)
            αav2 = math.degrees(αav22)

            εv1 = (zv1/(2*math.pi))*(math.tan(math.radians(αav1))-math.tan(math.radians(A_P)))
            εv2 = (zv2/(2*math.pi))*(math.tan(math.radians(αav2))-math.tan(math.radians(A_P)))

            εv = εv1 + εv2

            τP = βi - A_H

            εβ = (R/M)*τP

            εγ = εv + εβ

        except ValueError:
            QMessageBox.warning(None, "Erreur de saisie", "Il est impossible d'effectuer le calcul. Vérifiez les valeurs d'entrée.")
            return
        except ZeroDivisionError:
            QMessageBox.warning(None, "Erreur de saisie", "Il est impossible d'effectuer le calcul. Vérifiez les valeurs d'entrée.")
            return

